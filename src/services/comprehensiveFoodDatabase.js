// Function to get all categories
export const getFoodCategories = () => {
  const categories = new Set(comprehensiveFoods.map(food => food.category));
  return Array.from(categories).sort();
};

// Function to get foods by category
export const getFoodsByCategory = (category) => {
  return comprehensiveFoods.filter(food => food.category === category);
};

// Function to search foods
export const searchComprehensiveFoods = (query) => {
  const searchTerm = query.toLowerCase();
  return comprehensiveFoods.filter(food =>
    food.name.toLowerCase().includes(searchTerm) ||
    (food.brand && food.brand.toLowerCase().includes(searchTerm)) ||
    (food.category && food.category.toLowerCase().includes(searchTerm))
  );
};